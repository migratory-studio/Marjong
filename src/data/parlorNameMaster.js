// 解析：{ name, subs:[..] }。速度→speed 正規化＋不正トークン除去。subs が空なら read で埋める。
export const PARLOR_NAME_ENTRIES = RAW.trim().split("\n").map((line) => {
  const [name, raw] = line.split(",");
  let subs = String(raw || "").split(";").map(normSub).filter((s) => VALID_SUBS.has(s));
  if (subs.length === 0) subs = ["read"];
  if (subs.length > 2) subs = subs.slice(0, 2); // 念のため最大2
  return { name: name.trim(), subs };
});

// 単一副パラメの店だけ（楽勝・拮抗用）。
export const PARLOR_SINGLE = PARLOR_NAME_ENTRIES.filter((e) => e.subs.length === 1);
// 複数副パラメの店（チャレンジ・大会用に許容）。
export const PARLOR_MULTI = PARLOR_NAME_ENTRIES.filter((e) => e.subs.length >= 2);
